#include<stdio.h>

int main(void) {
  int i;
  double x,v,m,u,e,t,tmax,dt;
  double k0[2],k1[2],k2[2],k3[2],km,X[4];
  double x1,v1,m1,x2,v2,m2,x3,v3,m3;
  double G = 9.8*6400000*6400000;
  x = 0;
  v = 0;
  m = 12500;

  scanf("%lf %lf %lf %lf",&u,&e,&dt,&tmax);

  i=0;
  for(t=0; t<=tmax; t+=dt) {
    printf("%8.4f %8.4f %8.4f %8.4f\n",t,x,v,m);
    if(i == 0) {
      if(m > 3780) {
	X[0] = 6400000+x;
	k0[0] = dt*v;
	k0[1] = -dt*((G/(X[0]*X[0]))+(e*u/m));
	km = dt*(-e);
	x1 = x+k0[0]/2.0;
	v1 = v+k0[1]/2.0;
	m1 = m+km/2.0;
	X[1] = 6400000+x1;
	k1[0] = dt*v1;
	k1[1] = -dt*((G/(X[1]*X[1]))+(e*u/m1));
	x2 = x+k1[0]/2.0;
	v2 = v+k1[1]/2.0;
	m2 = m+km/2.0;
	X[2] = 6400000+x2;
	k2[0] = dt*v2;
	k2[1] = -dt*((G/(X[2]*X[2]))+(e*u/m2));
	x3 = x+k2[0];
	v3 = v+k2[1];
	m3 = m+km;
	X[3] = 6400000+x3;
	k3[0] = dt*v3;
	k3[1] = -dt*((G/(X[3]*X[3]))+(e*u/m3));
	x = x+(k0[0]+2.0*k1[0]+2.0*k2[0]+k3[0])/6.0;
	v = v+(k0[1]+2.0*k1[1]+2.0*k2[1]+k3[1])/6.0;
	m = m+km;
      }
      else {
	X[0] = 6400000+x;
	k0[0] = dt*v;
	k0[1] = -dt*(G/(X[0]*X[0]));
	x1 = x+k0[0]/2.0;
	v1 = v+k0[1]/2.0;
	X[1] = 6400000+x1;
	k1[0] = dt*v1;
	k1[1] = -dt*(G/(X[1]*X[1]));
	x2 = x+k1[0]/2.0;
	v2 = v+k1[1]/2.0;
	X[2] = 6400000+x2;
	k2[0] = dt*v2;
	k2[1] = -dt*(G/(X[2]*X[2]));
	x3 = x+k2[0];
	v3 = v+k2[1];
	X[3] = 6400000+x3;
	k3[0] = dt*v3;
	k3[1] = -dt*(G/(X[3]*X[3]));
	x = x+(k0[0]+2.0*k1[0]+2.0*k2[0]+k3[0])/6.0;
	v = v+(k0[1]+2.0*k1[1]+2.0*k2[1]+k3[1])/6.0;
      }
      if(x < 0) {
	x = 0;
	v = 0;
	i++;
      }
    }
    else {
      if(m > 3780) {
	m = m+km;
      }
      else {
	break;
      }
    }
  }
  return(0);
}
